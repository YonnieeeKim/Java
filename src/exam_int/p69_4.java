package exam_int;

public class p69_4 {
	public static void main(String[] args) {

		//정수와 실수의 사칙연산 결과를 정수로 출력
		int a = 10;
		double b = 2.0;
		int add = (int) (a + b);
		int sub = (int) (a - b);
		int mul = (int) (a * b);
		int div = (int) (a / b);

		System.out.println(add);
		System.out.println(sub);
		System.out.println(mul);
		System.out.println(div);

	}

}
